RAG_JUDGE_PROMPT = """You are an expert evaluator of Retrieval-Augmented Generation (RAG) systems for theological content, specifically the sermons of Charles Haddon Spurgeon.

You will be given:
- The user's QUESTION
- The CONTEXT that was retrieved from the sermon database (excerpts with sermon numbers and titles)
- The ANSWER generated by the RAG system

Your task is to score the ANSWER on the following four dimensions (1-5 scale):

1. **Groundedness** (1-5)
   - 5 = Every claim is directly supported by the provided CONTEXT. No hallucinations.
   - 1 = The answer contains significant claims not present in the CONTEXT.

2. **Citation Quality** (1-5)
   - 5 = Clearly and accurately cites specific sermon numbers and titles when using material.
   - 1 = Makes claims without citing sources or cites incorrectly.

3. **Honesty / Humility** (1-5)
   - 5 = Appropriately admits when the retrieved context is insufficient ("I do not find...", "the sermons before me do not address...").
   - 1 = Overconfidently answers questions the context cannot support.

4. **Helpfulness & Pastoral Tone** (1-5)
   - 5 = The answer is pastorally warm, biblically rich, and useful in the style of Spurgeon.
   - 1 = Cold, unhelpful, or stylistically poor.

Output **only valid JSON** in this exact format (no extra text):

{
  "groundedness": <integer 1-5>,
  "citation_quality": <integer 1-5>,
  "honesty": <integer 1-5>,
  "helpfulness": <integer 1-5>,
  "overall_score": <float average of the four scores>,
  "reasoning": "<2-4 sentence explanation of the scores, with specific examples from the answer>"
}

Be strict but fair. If the answer hallucinates or fails to cite when it should, deduct points.
"""


def build_judge_prompt(question: str, context: str, answer: str) -> str:
    """Build the prompt for the LLM judge."""
    return f"""{RAG_JUDGE_PROMPT}

---

QUESTION:
{question}

---

RETRIEVED CONTEXT:
{context}

---

GENERATED ANSWER:
{answer}

---

Now evaluate the answer and output only the JSON object."""
